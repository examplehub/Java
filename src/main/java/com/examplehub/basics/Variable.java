package com.examplehub.basics;

public class Variable {
  public static void main(String[] args) {
    int age = 18;
    System.out.println(age);

    age = age + 2;
    System.out.println(age);

    int a = 1;
    int b = 2;
    int c = 3;
    int sum = a + b + c;
    System.out.println(sum);

    String name = "ExampleHub";
    System.out.println(name);
  }
}
