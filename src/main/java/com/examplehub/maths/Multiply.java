package com.examplehub.maths;

public class Multiply {

    /**
     * Multiply two integer numbers.
     *
     * @param a the first integer number.
     * @param b the second integer number.
     * @return the multiply of two integer numbers.
     */
    public static int multiply(int a, int b) {
        return a * b;
    }

    /**
     * Multiply two double numbers.
     *
     * @param a the first double number.
     * @param b the second double number.
     * @return the multiply of two double numbers.
     */
    public static double multiply(double a, double b) {
        return a * b;
    }
}
